    private void guardarProveedor() {
        try {
            int id = Integer.parseInt(txtId.getText());
            String nombre = txtNombre.getText();
            String direccion = txtDireccion.getText();
            int telefono = Integer.parseInt(txtTelefono.getText());

            Proveedores proveedor = new Proveedores(id, nombre, direccion, telefono);
            proveedoresServicio.registrarProveedor(id, nombre, direccion, telefono);

            JOptionPane.showMessageDialog(this, "Proveedor creado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            dispose(); // Cerrar la ventana
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, ingresa valores válidos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

